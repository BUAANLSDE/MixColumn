package cn.kejso.Mix;

// union two table (structure same) to one
public class Union {
	
	private String  tableA;
	
	private String  tableB;
	
	private String  new_table;

	public String getTableA() {
		return tableA;
	}

	public void setTableA(String tableA) {
		this.tableA = tableA;
	}

	public String getTableB() {
		return tableB;
	}

	public void setTableB(String tableB) {
		this.tableB = tableB;
	}

	public String getNew_table() {
		return new_table;
	}

	public void setNew_table(String new_table) {
		this.new_table = new_table;
	}
	
}
